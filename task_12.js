/*
Создайте несколько функций, которые будут возвращать сумму, разность, модуль,
произведение, частное и показатель степени соответственно.
 */
function add(a,b){
    return a + b
}

function divide(a,b){
    return a / b
}

function multiply(a,b){
    return a * b
}

function mod(a,b){
    return a % b
}

function exponent(a,b){
    return a ** b
}

function subt(a,b){
    return a - b
}